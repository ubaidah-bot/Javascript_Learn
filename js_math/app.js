// JavaScript Math

console.log(Math.round(Math.PI));
console.log(Math.round(3.9));

console.log(Math.ceil(8.00001)); 
console.log(Math.floor(8.00001)); 


console.log(Math.pow(2, 2)); 
console.log(Math.pow(4, 2)); 


console.log(Math.sqrt(64)); 

// Max or Min value
console.log(Math.max(0, 5, 8, 9, 55, 87));
console.log(Math.min(0, 5, 8, 9, 55, 87));

// PI value
console.log(Math.PI);

// Algebra sin, cos value
console.log(Math.sin((90 * Math.PI) / 180));

// random value
console.log(Math.random());
console.log(Math.random().toFixed(2)); 
console.log(Math.random().toFixed(2) * 10); 
console.log(Math.random().toFixed(2) * 10 + 1); 
console.log(Math.ceil(Math.random()));
